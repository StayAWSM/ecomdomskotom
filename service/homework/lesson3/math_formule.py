print('Квадрат разности = ', a ** 2 - 2 * a * b + b ** 2)
print('Разность квадратов = ', (a + b) * (a - b))
print('разность кубов = ', (a - b) * (a ** 2 + a * b + b ** 2))
print('сумма кубов = ', (a + b) * (a ** 2 - a * b + b ** 2))
print('куб суммы = ', a ** 3 + 3 * a ** 2 * b + 3 * a * b ** 2 + b ** 3)
print('куб разности = ', a ** 3 - 3 * a ** 2 * b + 3 * a * b ** 2 - b ** 3)
